// Die in deisem Projekt angebotenen Routinen dienen als Grundlage 
// zur Programmierung des RobotCars Dyvelbot.
// Kurzerklärung der einzelnen Routinen:
// - main.h: Hauptprogramm mit Setup und Loop
// - motor.h: Definition der Motoren und deren Steuerung zur Bewegung des Roboters
// - motorcontrol.h: Steuerung der Motoren zum Fahren des Roboters auf dem gewünschten Kurs
// - liniensensor.h: Ermittlung der Sensorwerte des linken und rechten Liniensensors
// - entfernungssensor.h: Ermittlung der Sensorwerte des Abstandssensors (Ultraschallsensor)
// - helligkeitssensor.h: Ermittlung der Sensorwerte des Helligkeitssensors (LDR)
// - lampen.h: Steuerung der Lampen (LED-Stripe mit WS2812B - RGB-LEDs)
// - hupen.h: Steuerung der Hupe (Buzzer)
// - batterie.h: Ermittlung der Batteriespannung einer 9V Blockbatterie
//
